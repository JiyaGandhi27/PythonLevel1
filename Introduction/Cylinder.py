pi = 22/7
radius = int(input("Enter the radius of cylinder:"))
height = int(input("Enter the height of cylinder:"))
surfacevolume = pi*radius**2*height
print("The surface volume of cylinder is:", surfacevolume)

surfacearea = (2*pi*radius*height)+(2*pi*radius**2)
print("The surface area of cylinder is:", surfacearea)